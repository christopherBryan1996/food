const server = require('./src/app.js');
const { conn, Diet } = require('./src/db.js');
const tiposDieta=['Gluten Free','Ketogenic','Vegetarian','Lacto-Vegetarian','Ovo-Vegetarian','Vegan','Pescetarian','Paleo','Primal','Whole30']
const  { v4: uuidv4} =require('uuid')
let  id=0

// Syncing all the models at once.
conn.sync({ force: false }).then(() => {
  server.listen(3001, () => {
    console.log('%s listening at 3001'); // eslint-disable-line no-console
    //console.log(conn.models.Diet)
    tiposDieta.map((tipo)=>{
      Diet.findAll({
        where:{name:tipo}
      }).then(e=>{
        if(e.length==0){
          id+=1
          Diet.create({
            id: id,
            name:tipo
          }).then(()=>console.log('llenado de DB diet'))
          .catch((e)=>console.error(e))
        }
        console.log('ya estan creadas')
      }).catch(e=>console.error('error'))
    })
  });
});
